// Define additional people properties. 
function addPeopleProperties() {

	// 
	for(let faculty of facultyData) {
	
		// Set full name as title. 
		faculty.title = getFullName(faculty);

		// Set bio as post content. 
		faculty.content = getBio(faculty);
	
		// Set email address. 
		faculty.email = getEmailAddress(faculty);
	
		// Set phone number. 
		faculty.phone = '2134567890';
		faculty.phone = {countrycode:'1',areacode:'213',number:'4567890'};
		
		// Set searchable faculty data. 
		faculty.searchtags = getFacultyTags(faculty);
	}

	// 
	for(let student of studentData) {
	
		// Set full name as title. 
		student.title = getFullName(student);

		// Set bio as post content. 
		student.content = getBio(student);
	
		// Set email address. 
		student.email = getEmailAddress(student);
		
		// Set searchable student data. 
		student.searchtags = getStudentTags(student);

		// Assign a random program to each student. 
		student.programid = programData[Math.floor( (programData.length) * Math.random() )].programid;
	}

	/****/

	// Define bio of given person. 
	function getBio(person) {
		// 
		return `This is a brief bio about ${person.title}. Lorem ipsum dolor sit amet consectetur adipisicing elit. Delectus, alias soluta, debitis dolore porro sapiente quae cumque saepe facere quaerat eius, itaque aspernatur eos repellat reprehenderit illo reiciendis tempora.`;
	}

	// Define email address of given person. 
	function getEmailAddress(person) {
		// Get first initial. 
		let f = (person.firstname).charAt(0).toLowerCase();
		// Get last name. 
		let lname = (person.lastname).toLowerCase();

		// Compile and return email address. 
		return `${f}${lname}@true.edu`;
	}

	// Define full name of given person. 
	function getFullName(person) {
	
		// Get first name of person. 
		let fname = person.firstname;
	
		// Get last name of person. 
		let lname = person.lastname;
	
		// Get person id. 
		// let id = person.personid;
	
		// Compile components of full name. 
		return `${fname} ${lname}`;
		// return `${fname} ${lname} [${id}]`;
	}
	
	// Define searchable student tags. 
	function getStudentTags(person) {
	
		// Get first name of student. 
		let fname = person.firstname;
	
		// Get last name of student. 
		let lname = person.lastname;

		// Get id of given program. 
		let progid = person.programid; // console.log(progid);

		// Get name of given program. 
		let progname = getProgramById(progid).title;

		// Get bio of given student. 
		let bio = person.content;
	
		// Compile searchable components for this post type: person. 
		let tagsources = [ lname, fname, bio, progid, progname ];
		
		// Return string of tags. 
		return tagsources.join(' ').split(' ');
	}
	
	// Define searchable faculty tags. 
	function getFacultyTags(person) {
	
		// Get first name of faculty. 
		let fname = person.firstname;
	
		// Get last name of faculty. 
		let lname = person.lastname;

		// Get bio of given faculty. 
		let bio = person.content;
	
		// Compile searchable components for this post type: person. 
		let tagsources = [ lname, fname, bio, person.programids.join(' '), person.programids.map( (progid)=>(getProgramById(progid).title) ).join(' ') ];
		
		// Return string of tags. 
		return tagsources.join(' ').split(' ');
	}
}

