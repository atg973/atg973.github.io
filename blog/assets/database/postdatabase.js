// Get post index by post id. 
function getPostIndexById(id) {

	// Go thru all posts. 
	for(let i in blogDataList) {

		// Get current post. 
		let post = blogDataList[i];

		// Return index if post found. 
		if(post.postid==id) return i;
	}

	// Return nothing if post not found. 
	return -1;
}

// Get post by id. 
function getPostById(id) {

	// Go thru all posts. 
	for(let post of blogDataList) {

		// Return post if found. 
		if(post.postid==id) return post;
	}

	// Return nothing if post not found. 
	return null;
}

// Define sort function. 
function sortPosts(a,b) {

	// Initialize result. 
	let result = 0;

	// 
	let pA = a.published;
	let pB = b.published;

	// Add year differential. 
	result += 366 * (pB.year-pA.year);

	// Add month differential. 
	result += 31 * (pB.month-pA.month);

	// Add date differential. 
	result += (pB.date-pA.date);

	// Return result. 
	return result;
}

// Get post filter for given tag. 
function getPostFilter(tag,tagtitle) {

	// Initialize result. 
	let result = {
		title:tagtitle,
		criteria:[
			// 'tagtitle',
		],
	};

	// Save distinct values for given tag from blog post data to filtercriteria list. 
	for(let blogpost of blogDataList) {

		// Get value of tag for given post. 
		let value = blogpost[tag];

		// Check if already there. 
		let alreadyThere = result['criteria'].includes(value);
		// Save value for given post if not already there. 
		if(!alreadyThere) result['criteria'].push(value);
	}

	// Return result. 
	return result;
}

